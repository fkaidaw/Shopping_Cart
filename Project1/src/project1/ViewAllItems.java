    public ArrayList<Product> search(String query) 
    {
        ArrayList<Product> searchResult = new ArrayList<Product>();
        
        for (Product p : getProductList())
        {
            if (p.search(query))
            {
                searchResult.add(p);
            }
        }
        
        return searchResult;
    }

